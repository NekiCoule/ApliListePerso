public class Games {

    private Integer gameIcon;
    private String gameName;
    private String gameAnnee;
    private String gameDescription;

    public Integer getGameIcon() { return gameIcon; }
    public void setGameIcon(Integer monIcone) { this.gameIcon = monIcone; }

    public String getGameName() { return gameName; }
    public void setGameName(String monNom) { this.gameName = monNom; }

    public String getGameAnnee() { return gameAnnee; }
    public void setGameAnnee( String monAnnee) { this.gameAnnee = monAnnee; }

    public String getGameDescription() { return gameDescription; }
    public void setGameDescription(String maDescription) { this.gameDescription = maDescription; }

}
